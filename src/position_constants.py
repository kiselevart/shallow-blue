SQUARE_MASKS = [1 << i for i in range(64)]

FILE_MASKS = {}
for i in range(8):  
    mask = 0
    for rank in range(8):  
        square = rank * 8 + i
        mask |= 1 << square
    FILE_MASKS[i] = mask

RANK_MASKS = {}
for rank in range(8):  
    mask = 0
    for file in range(8):  
        square = rank * 8 + file
        mask |= 1 << square
    RANK_MASKS[rank] = mask